#2진수, 8진수, 16진수
0b110 # 6, 2진수는 숫자 앞에 0b를 붙이며 0과 1을 사용한다
0o10 #8, 8진수는 숫자 앞에 0o를 붙이며 0과 7까지 사용한다
0xF #숫자 앞에 0x 또는 0X를 붙이며 0부터 9, A부터 F까지 사용한다.(소문자 a부터 f도 가능)
