#실수와 정수를 함께 계산하면?
4.2 + 5 #9.2, 실수와 정수를 함께 계산하면 표현 범위가 넓은 실수로 계산된다(실수가 정수보다 표현 범위가 넓습니다)

#값을 실수로 만들기
float(5) #5.0
float(1 + 2)#3.0
float('5.3')#5.3

